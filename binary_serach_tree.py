class Node:
    def __init__(self, val):
        self.val = val
        self.left = None
        self.right = None

    def get(self):
        return self.val
    
    def set(val):
        self.val = val

class BST:
    def __init__(self):
        self.root = None
    
    def set_root(self, val):
        self.root = Node(val)